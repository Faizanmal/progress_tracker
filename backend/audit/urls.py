"""
URL configuration for audit app.
"""
from django.urls import path, include
from rest_framework.routers import DefaultRouter

from .views import AuditLogViewSet, ChangeSnapshotViewSet, AuditLogSearchViewSet

router = DefaultRouter()
router.register(r'logs', AuditLogViewSet, basename='audit-log')
router.register(r'snapshots', ChangeSnapshotViewSet, basename='snapshot')
router.register(r'saved-searches', AuditLogSearchViewSet, basename='saved-search')

urlpatterns = [
    path('', include(router.urls)),
]
